#include <bits/stdc++.h>
using namespace std;

int main() {
    int A, B;
    cin >> A >> B;

    // ここにプログラムを追記

    int i = 0;

    cout << "A:";
    while (i < A) {
        cout << "]";
        i++;
    }
    cout << endl
         << "B:";

    i = 0;
    while (i < B) {
        cout << "]";
        i++;
    }
    cout << endl;
}
