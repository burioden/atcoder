#include <bits/stdc++.h>
using namespace std;

int main() {
    // 一年の秒数
    int seconds = 365 * 24 * 60 * 60;

    cout << seconds << endl;      /* 1年は何秒か */
    cout << seconds * 2 << endl;  /* 2年は何秒か */
    cout << seconds * 5 << endl;  /* 5年は何秒か */
    cout << seconds * 10 << endl; /* 10年は何秒か */
}
